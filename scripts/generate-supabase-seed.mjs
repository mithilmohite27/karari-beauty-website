import { Buffer } from "node:buffer";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");
const outputPath = path.join(projectRoot, "supabase", "seed.sql");

async function importDataFile(relativePath) {
  const absolutePath = path.join(projectRoot, relativePath);
  const source = await readFile(absolutePath, "utf8");
  const encoded = Buffer.from(`${source}\n//# sourceURL=${pathToFileURL(absolutePath).href}`).toString("base64");
  return import(`data:text/javascript;base64,${encoded}`);
}

function slugify(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)+/g, "");
}

function sqlString(value) {
  if (value === null || value === undefined || value === "") return "null";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlNumber(value, fallback = 0) {
  const number = Number(value);
  return Number.isFinite(number) ? number.toFixed(2) : Number(fallback).toFixed(2);
}

function sqlRating(value) {
  const number = Number(value);
  return Number.isFinite(number) ? number.toFixed(1) : "null";
}

function sqlBoolean(value) {
  return value ? "true" : "false";
}

function sqlTextArray(values) {
  const items = Array.isArray(values) ? values : [];
  if (!items.length) return "'{}'";
  return `array[${items.map(sqlString).join(", ")}]::text[]`;
}

function sqlJson(value) {
  return sqlString(JSON.stringify(value || {}));
}

function normalizeProduct(product, categoriesBySlug, categoriesByName, index) {
  const requestedSlug = product.categorySlug || slugify(product.category);
  const category = categoriesBySlug.get(requestedSlug) || categoriesByName.get(product.category) || categoriesBySlug.get("gift-items");
  const categorySlug = category?.slug || requestedSlug || "gift-items";

  return {
    ...product,
    slug: product.slug || slugify(product.name),
    categoryName: product.category || category?.name || "Gift Items",
    categorySlug,
    originalPrice: product.originalPrice || Math.ceil(Number(product.price || 0) * 1.18),
    discountLabel: product.discountLabel || product.offer || "Karari Edit",
    rating: product.rating || (4.7 + (index % 3) / 10).toFixed(1),
    shortDescription: product.shortDescription || product.description,
    isFeatured: product.isFeatured ?? index < 18,
    tags: product.tags || [categorySlug],
    sortOrder: index
  };
}

const [{ categories }, { products }] = await Promise.all([
  importDataFile("data/categories.js"),
  importDataFile("data/products.js")
]);

const categoriesBySlug = new Map(categories.map((category) => [category.slug, category]));
const categoriesByName = new Map(categories.map((category) => [category.name, category]));
const normalizedProducts = products.map((product, index) => normalizeProduct(product, categoriesBySlug, categoriesByName, index));

