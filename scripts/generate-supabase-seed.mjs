    "  tags = excluded.tags,\n" +
    "  is_featured = excluded.is_featured,\n" +
    "  is_active = excluded.is_active,\n" +
    "  stock_status = excluded.stock_status,\n" +
    "  sort_order = excluded.sort_order;"
